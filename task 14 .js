// Values
let a = 10;
let b = 5;

console.log("Arithmetic Operators");
console.log("Addition:", a + b);
console.log("Subtraction:", a - b);
console.log("Multiplication:", a * b);
console.log("Division:", a / b);
console.log("Modulus:", a % b);

console.log("\n Comparison Operators");
console.log("a == b:", a == b);
console.log("a === b:", a === b);
console.log("a != b:", a != b);
console.log("a > b:", a > b);
console.log("a < b:", a < b);
console.log("a >= b:", a >= b);
console.log("a <= b:", a <= b);

console.log("\nLogical Operators");
let x = true;
let y = false;

console.log("x AND y:", x && y);
console.log("x OR y:", x || y);
console.log("NOT x:", !x);

console.log("\n Assignment Operators");
let c = 10;

c += 5;   // c = c + 5
console.log("c += 5:", c);

c -= 3;   // c = c - 3
console.log("c -= 3:", c);

c *= 2;   // c = c * 2
console.log("c *= 2:", c);

c /= 4;   // c = c / 4
console.log("c /= 4:", c);

c %= 3;   // c = c % 3
console.log("c %= 3:", c);